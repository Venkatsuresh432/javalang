public class PutCalc {
    static int a,b;
//  --------------------------------------------------------------------------------------------------------------------
    public static int add1(int b1,int b2)
    {
        a=b1;
        b=b2;
        return a+b;
    }
//  --------------------------------------------------------------------------------------------------------------------
    public static int sub1(int c1,int c2)
    {
        a=c1;
        b=c2;
        return a-b;
    }
//  --------------------------------------------------------------------------------------------------------------------
    public static int mul1(int d1,int d2)
    {
        a=d1;
        b=d2;
        return a*b;
    }
//  --------------------------------------------------------------------------------------------------------------------
    public static int div1(int a1,int a2)
    {
        a=a1;
        b=a2;
        return a/b;
    }
}
