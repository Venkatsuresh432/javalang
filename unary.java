public class unary {
    public static  void main(String[] args){
        int a=12;
        System.out.println(++a);
        System.out.println("op:"+a);
//        System.out.println();
        System.out.println(a++);
        System.out.println("op:"+a);
        System.out.println(--a);
        System.out.println("op:"+a);
        System.out.println(a--);
        System.out.println("op:"+a);
    }
}
